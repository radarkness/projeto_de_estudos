# IN =  significa se há o valor definido na variavel, caso seja positivo será TRUE caso contrário será FALSE
# NOT IN = significa se não há o valor definido na variável, caso seja negativo será TRUE caso contrário sera FALSE

frutas = ["laranja", "maçã", "manga", "limão"]
curso = "curso de python"
valores = 10, 100, 1000


print("banana" not in frutas)
print("laranja" in frutas)
print("python" in curso)
print(100 not in valores)